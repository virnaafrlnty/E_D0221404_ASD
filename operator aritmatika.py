# operator aritmatika
a = 10
b = 2

# operasi penjumlahan +
hasil = a + b
print(a, '+', b, '=',  hasil)

# operasi pengurangan -
hasil = a - b
print(a, '-', b, '=',  hasil)

# operasi pembagian /
hasil = a / b
print(a, '/', b, '=',  hasil)

# operasi perkalian *
hasil = a * b
print(a, '*', b, '=',  hasil)

# operasi sisa bagi %
hasil = a + b
print(a, '+', b, '=',  hasil)

# operasi perpangkatan **
hasil = a ** b
print(a, '**', b, '=',  hasil)

# operasi pembagian bulat +
hasil = a // b
print(a, '//', b, '=',  hasil)