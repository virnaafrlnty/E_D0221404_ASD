# operator penugasan (=)
x = 5

# penjumlahan/ tambah samadengan +=
x += 3 #x = x + 3
print(x)

# pengurangan/ kurang samadengan -=
x -= 3 #x = x - 3
print(x)

# pembagian/ bagi samadengan /=
x /= 3 #x = x / 3
print(x)

# perkalian/ kali samadengan *=
x *= 3 #x = x * 3
print(x)

# sisa bagi/ sisa bagi samadengan %=
x %= 3 #x = x % 3
print(x)

# pangkat samadengan **=
x **= 3 #x = x ** 3
print(x)

# pembagian bulat samadengan //=
x //= 3 #x = x // 3
print(x)